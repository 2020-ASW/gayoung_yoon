def solution(n, money):

    board = [[0 for _ in range(n+1)] for _ in range(len(money))]

    for i in range(0, n+1, money[0]):
        board[0][i] = 1
    # print(board)
    # print('-----------------------------------------------------')

    for x in range(1, len(money)):
        for y in range(n+1):
            if y >= money[x]:
                board[x][y] = board[x-1][y] + board[x][y-money[x]]
            else:
                board[x][y] = board[x-1][y]
            # print(board)
            # print('===================================================================')

    return board[-1][-1]


